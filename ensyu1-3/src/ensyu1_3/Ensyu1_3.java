package ensyu1_3;
//右に示すように、各行に1文字ずつ名前を表示するプログラムを作成せよ。姓と名の間は1行あけることとし、自分の名前を表示すること
public class Ensyu1_3 {

	public static void main(String[] args) {
		//名前の一文字目を表示
		System.out.println("宮");
		//名前の二文字目を表示
		System.out.println("岡");
		//姓と名の間に一行あける
		System.out.println("");
		//名前の三文字目を表示
		System.out.println("達");
		//名前の四文字目を表示
		System.out.println("野");
	}

}
