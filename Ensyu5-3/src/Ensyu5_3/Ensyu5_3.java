package Ensyu5_3;
//倫理型の変数にtrueやFalseを代入して、その値を表示するプログラムを作成せよ。
public class Ensyu5_3 {

	public static void main(String[] args) {

		//論理型の変数にtrueを代入する
		boolean booleanT = true;
		//論理型の変数にFalseを代入する
		boolean booleanF = false;

		//代入した値を表示する
		System.out.println("trueを表示：" + booleanT);
		//代入した値を表示する
		System.out.println("falseを表示：" + booleanF);
	}

}
